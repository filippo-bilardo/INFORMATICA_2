#include <iostream>

int main() {
    // Dichiarazione di una variabile per memorizzare l'input
    int numero;
    // Utilizzo di cin per leggere un numero dalla console
    std::cout << "Inserisci un numero intero: ";
    std::cin >> numero;
    // Output del numero inserito
    std::cout << "Hai inserito il numero: " << numero << std::endl;

    return 0;
}
